def _encode_clause(clause: ThresholdNoGoodClause) -> _EncodedClause:
    maximum_variable = max(abs(literal) for literal in clause.literals)
    positive_bytes = bytearray((maximum_variable + 7) // 8)
    negative_bytes = bytearray((maximum_variable + 7) // 8)
    for literal in clause.literals:
        position = abs(literal) - 1
        byte_index, bit_index = divmod(position, 8)
        if literal > 0:
            positive_bytes[byte_index] |= 1 << bit_index
        else:
            negative_bytes[byte_index] |= 1 << bit_index
    return _EncodedClause(
        positive=int.from_bytes(positive_bytes, "little"),
        negative=int.from_bytes(negative_bytes, "little"),
    )


def _resolve_exact(
    left: ThresholdNoGoodClause,
    right: ThresholdNoGoodClause,
    *,
    pivot: int,
) -> ThresholdNoGoodClause:
    if isinstance(pivot, bool) or not isinstance(pivot, int) or pivot <= 0:
        raise O1C104PreparationError("resolution pivot differs")
    left_pivot = {literal for literal in left.literals if abs(literal) == pivot}
    right_pivot = {literal for literal in right.literals if abs(literal) == pivot}
    if (left_pivot, right_pivot) not in (({-pivot}, {pivot}), ({pivot}, {-pivot})):
        raise O1C104PreparationError("resolution complementary pivot differs")
    left_tail = {literal for literal in left.literals if abs(literal) != pivot}
    right_tail = {literal for literal in right.literals if abs(literal) != pivot}
    if any(-literal in right_tail for literal in left_tail):
        raise O1C104PreparationError("resolution has a non-pivot complement")
    try:
        return ThresholdNoGoodClause(tuple(sorted(left_tail | right_tail, key=abs)))
    except ThresholdNoGoodVaultError as exc:
        raise O1C104PreparationError("resolution canonical clause differs") from exc


def _pair_resolution(
    left: _ClauseRef,
    right: _ClauseRef,
    encoded: Mapping[int, _EncodedClause],
) -> tuple[str, int | None, ThresholdNoGoodClause | None]:
    left_bits = encoded[id(left.clause)]
    right_bits = encoded[id(right.clause)]
    complements = (left_bits.positive & right_bits.negative) | (
        left_bits.negative & right_bits.positive
    )
    count = complements.bit_count()
    if count == 0:
        return "zero", None, None
    if count != 1:
        return "multi", None, None
    pivot = complements.bit_length()
    return "single", pivot, _resolve_exact(left.clause, right.clause, pivot=pivot)


