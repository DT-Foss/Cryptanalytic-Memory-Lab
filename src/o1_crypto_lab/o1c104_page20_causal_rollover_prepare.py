class O1C104PreparationError(RuntimeError):
    """An O1C-0103 seal or deterministic Page-20 invariant differs."""


def lab_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _mapping(value: object, label: str) -> Mapping[str, object]:
    if not isinstance(value, Mapping) or not all(isinstance(key, str) for key in value):
        raise O1C104PreparationError(f"{label} differs")
    return cast(Mapping[str, object], value)


def _sequence(value: object, label: str) -> Sequence[object]:
    if not isinstance(value, Sequence) or isinstance(value, (str, bytes, bytearray)):
        raise O1C104PreparationError(f"{label} differs")
    return cast(Sequence[object], value)


def _canonical_path(value: str | Path, label: str, *, directory: bool) -> Path:
    path = Path(value)
    if not path.is_absolute():
        raise O1C104PreparationError(f"{label} path is not canonical")
    try:
        metadata = path.lstat()
        resolved = path.resolve(strict=True)
    except OSError as exc:
        raise O1C104PreparationError(f"{label} is unreadable") from exc
    expected = (
        stat.S_ISDIR(metadata.st_mode) if directory else stat.S_ISREG(metadata.st_mode)
    )
    if stat.S_ISLNK(metadata.st_mode) or not expected or path != resolved:
        raise O1C104PreparationError(f"{label} path is not canonical")
    return path


def _canonical_document(payload: bytes, label: str) -> Mapping[str, object]:
    try:
        value = _mapping(json.loads(payload), label)
    except (UnicodeError, json.JSONDecodeError) as exc:
        raise O1C104PreparationError(f"{label} differs") from exc
    if canonical_json_bytes(value) != payload:
        raise O1C104PreparationError(f"{label} is not canonical JSON")
    return value


@dataclass(frozen=True)
class ComposedPage20Projection:
    """Authoritative Page-20 projection across emitted and derived namespaces."""

    lineage_ordinal: int
    vault: ThresholdNoGoodVault
    base_selector_projection: ResidencyProjection
    selected_emitted_union_indices: tuple[int, ...]
    priority_selected_emitted_union_indices: tuple[int, ...]
    selected_inherited_derived_clauses: tuple[Mapping[str, object], ...]
    selected_new_derived_clauses: tuple[Mapping[str, object], ...]
    displaced_emitted_union_indices: tuple[int, ...]
    category_counts: Mapping[str, int]
    document: Mapping[str, object]

    def __post_init__(self) -> None:
        encoding = _mapping(self.document.get("encoding_only"), "Page-20 encoding")
        base_selected = frozenset(self.base_selector_projection.selected_union_indices)
        selected = frozenset(self.selected_emitted_union_indices)
        displaced = frozenset(self.displaced_emitted_union_indices)
        if (
            self.lineage_ordinal != PAGE20_LINEAGE_ORDINAL
            or self.vault.sha256 != PAGE20_SHA256
            or self.vault.clause_count != PAGE20_ACTIVE_LIMIT
            or self.base_selector_projection.lineage_ordinal != PAGE20_LINEAGE_ORDINAL
            or self.base_selector_projection.vault.sha256 != PAGE20_BASE_SHA256
            or self.base_selector_projection.vault == self.vault
            or tuple(sorted(self.selected_emitted_union_indices))
            != self.selected_emitted_union_indices
            or len(self.selected_emitted_union_indices) != 192
            or len(self.priority_selected_emitted_union_indices) != 192
            or set(self.priority_selected_emitted_union_indices) != selected
            or len(self.displaced_emitted_union_indices) != 55
            or len(displaced) != 55
            or selected.intersection(displaced)
            or selected.union(displaced) != base_selected
            or len(self.selected_inherited_derived_clauses)
            != INHERITED_DERIVED_OVERLAY_COUNT
            or len(self.selected_new_derived_clauses)
            != NEW_DERIVED_OVERLAY_CLAUSE_COUNT
            or sum(self.category_counts.values()) != PAGE20_ACTIVE_LIMIT
            or encoding != self.vault.describe()
            or self.document.get("maximum_clause_count") != PAGE20_ACTIVE_LIMIT
            or self.document.get("selected_emitted_union_indices")
            != list(self.selected_emitted_union_indices)
            or self.document.get("priority_selected_emitted_union_indices")
            != list(self.priority_selected_emitted_union_indices)
            or self.document.get("selected_inherited_derived_clauses")
            != list(self.selected_inherited_derived_clauses)
            or self.document.get("selected_new_derived_clauses")
            != list(self.selected_new_derived_clauses)
            or self.document.get("displaced_emitted_union_indices")
            != list(self.displaced_emitted_union_indices)
            or self.document.get("category_counts") != self.category_counts
        ):
            raise O1C104PreparationError("authoritative Page-20 projection differs")

    @property
    def selected_union_indices(self) -> tuple[int, ...]:
        """Compatibility view: only emitted clauses possess attic union indices."""

        return self.selected_emitted_union_indices

    @property
    def selection_order(self) -> tuple[int, ...]:
        """Compatibility view of the emitted selector's priority order."""

        return self.priority_selected_emitted_union_indices

    def describe(self) -> Mapping[str, object]:
        return self.document


@dataclass(frozen=True)
class ComposedPage20State:
    """Authoritative composed state with an explicit pure-causal base selector."""

    emitted_state: CausalResidencyState
    current_projection: ComposedPage20Projection
    residency_payload: bytes
    activation_payload: bytes

    def __post_init__(self) -> None:
        residency = _canonical_document(
            self.residency_payload, "authoritative composed residency"
        )
        activation = _canonical_document(
            self.activation_payload, "authoritative composed activation"
        )
        selector = _mapping(
            residency.get("emitted_selector_candidate"),
            "authoritative emitted selector",
        )
        current = _mapping(
            residency.get("current_projection"),
            "authoritative current projection",
        )
        activation_in_residency = _mapping(
            residency.get("activation_ledger"),
            "authoritative activation ledger",
        )
        entries = _sequence(
            activation.get("composed_entries"), "authoritative composed entries"
        )
        if len(entries) != 1:
            raise O1C104PreparationError("authoritative composed entry differs")
        entry = _mapping(entries[0], "authoritative composed entry")
        used = tuple(
            cast(
                Sequence[str],
                _sequence(
                    activation.get("used_active_sha256"),
                    "authoritative used active identities",
                ),
            )
        )
        base_sha256 = self.emitted_state.active_projection.sha256
        if (
            self.emitted_state.current_projection.lineage_ordinal
            != PAGE20_LINEAGE_ORDINAL
            or self.emitted_state.active_limit != PAGE20_ACTIVE_LIMIT
            or base_sha256 != PAGE20_BASE_SHA256
            or self.current_projection.lineage_ordinal != PAGE20_LINEAGE_ORDINAL
            or self.current_projection.vault.sha256 != PAGE20_SHA256
            or residency.get("schema") != COMPOSED_RESIDENCY_SCHEMA
            or len(self.residency_payload) != PAGE20_RESIDENCY_DOCUMENT_BYTES
            or sha256_bytes(self.residency_payload) != PAGE20_RESIDENCY_DOCUMENT_SHA256
            or activation.get("schema") != COMPOSED_ACTIVATION_SCHEMA
            or len(self.activation_payload) != PAGE20_ACTIVATION_DOCUMENT_BYTES
            or sha256_bytes(self.activation_payload)
            != PAGE20_ACTIVATION_DOCUMENT_SHA256
            or residency.get("active_limit") != PAGE20_ACTIVE_LIMIT
            or residency.get("lineage_ordinal") != PAGE20_LINEAGE_ORDINAL
            or current != self.current_projection.document
            or activation_in_residency != activation
            or selector.get("encoding_only")
            != self.emitted_state.active_projection.describe()
            or selector.get("activated") is not False
            or activation.get("forbidden_nonactivated_candidate_sha256") != base_sha256
            or activation.get("pure_emitted_candidate_activated") is not False
            or base_sha256 in used
            or not used
            or used[-1] != PAGE20_SHA256
            or entry.get("lineage_ordinal") != PAGE20_LINEAGE_ORDINAL
            or entry.get("role")
            != "composed-causal-page-with-two-certified-resolution-namespaces"
            or entry.get("active_sha256") != PAGE20_SHA256
            or entry.get("selected_emitted_union_indices")
            != list(self.current_projection.selected_emitted_union_indices)
            or entry.get("selected_inherited_derived_clauses")
            != list(self.current_projection.selected_inherited_derived_clauses)
            or entry.get("selected_new_derived_clauses")
            != list(self.current_projection.selected_new_derived_clauses)
            or residency.get("emitted_causal_attic")
            != self.emitted_state.attic.describe()
        ):
            raise O1C104PreparationError("authoritative composed state differs")

    @property
    def attic(self) -> CausalAttic:
        return self.emitted_state.attic

    @property
    def causal_base_state(self) -> CausalResidencyState:
        """Compatibility alias for the explicitly non-authoritative selector state."""

        return self.emitted_state

    @property
    def active_limit(self) -> int:
        return PAGE20_ACTIVE_LIMIT

    @property
    def active_projection(self) -> ThresholdNoGoodVault:
        return self.current_projection.vault

    @property
    def base_selection_metadata(self) -> Mapping[str, object]:
        residency = _canonical_document(
            self.residency_payload, "authoritative composed residency"
        )
        return _mapping(
            residency.get("emitted_selector_candidate"),
            "authoritative emitted selector",
        )

    @property
    def used_active_sha256(self) -> tuple[str, ...]:
        activation = _canonical_document(
            self.activation_payload, "authoritative composed activation"
        )
        return tuple(
            cast(
                Sequence[str],
                _sequence(
                    activation.get("used_active_sha256"),
                    "authoritative used active identities",
                ),
            )
        )

    def describe(self) -> Mapping[str, object]:
        return _canonical_document(
            self.residency_payload, "authoritative composed residency"
        )

    def activation_ledger_document(self) -> Mapping[str, object]:
        return _canonical_document(
            self.activation_payload, "authoritative composed activation"
        )


